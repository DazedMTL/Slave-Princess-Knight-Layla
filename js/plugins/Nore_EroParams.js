var ACME_VAR_ID = 35;
function upEroParam(actorId, commonId, taneoyaId, skipNinshinEvent) {
  if (skipNinshinEvent === void 0) {
    skipNinshinEvent = false;
  }
  var key = actorId + "_" + commonId;
  console.log("upEroParam:" + key);
  var params = ERO_PARAMS[key];
  if (!params) {
    console.error(key + "の ERO_PARAMS が見つかりません");
    return;
  }
  if ($gameSwitches.value(999)) {
    return;
  }
  var actorHistory = $gameSystem.historyManager().getActorHistory(actorId);
  var history = actorHistory.lastHistory();
  var lastNinshinRate = actorHistory.countNinshinRate($gameSystem.day());
  actorHistory.addEroExperience(taneoyaId);
  var plus = 0;
  var variance = params.variance;
  if (variance > 0) {
    plus += Math.randomInt(variance);
  }
  var isEroUp = true;
  if (actorId == 6 && !$gameSwitches.value(962)) {
    isEroUp = false;
  }
  var schedule = null;
  if (params.nakadashi > 0) {
    switch (params.type) {
      case ScheduleType.TANETSUKE:
      case ScheduleType.TANETSUKE_BACK:
        schedule = registerSchedule(
          history,
          ScheduleType.TANETSUKE,
          params.nakadashi + plus
        );
        history.addSchedule(ScheduleType.SEX_PERSON, taneoyaId, null);
        break;
      case ScheduleType.SEX_TO1:
      case ScheduleType.SEX_TO3:
      case ScheduleType.SEX_TO7:
        break;
      default:
        history.addSchedule(ScheduleType.SEX_PERSON, taneoyaId, null);
        schedule = registerSchedule(
          history,
          ScheduleType.NAKADASHI,
          params.nakadashi + plus
        );
        break;
    }
  }
  if (params.anal > 0) {
    registerSchedule(history, ScheduleType.ANAL, params.anal + plus);
  }
  var actor = $gameActors.actor(actorId);
  if (params.ninshin && actor.boteId == 0) {
    registerSchedule(
      history,
      ScheduleType.KYOSEI_NINSHIN,
      params.ninshin,
      true,
      key
    );
    taneoyaId = taneoyaId || params.ninshin;
    p("upEroParam　妊娠: 種親" + taneoyaId);
    if (actorId != 7) {
      if (taneoyaId > 0) {
        if (!skipNinshinEvent) {
          $gameSystem
            .timelineManager()
            .push(new TimelineItem(TimelineType.NINSHIN, 0, actorId));
        }
      } else {
        console.error("upEroParam　強制妊娠 で種親が設定されていません");
      }
    }
  }
  switch (params.type) {
    case ScheduleType.FELA_TO7:
    case ScheduleType.FELA_BY1:
      registerSchedule(history, params.type, 1);
      break;
    case ScheduleType.TANETSUKE_DAIJIN:
    case ScheduleType.TANETSUKE_GRAY:
      registerSchedule(history, params.type, params.nakadashi);
      break;
    case ScheduleType.SEX_TO1:
    case ScheduleType.SEX_TO3:
    case ScheduleType.SEX_TO7:
    case ScheduleType.SEX_TO1_AND_3:
      registerSchedule(history, params.type, params.nakadashi);
      break;
    case ScheduleType.CHICHI:
      registerSchedule(history, params.type, params.nakadashi);
      break;
  }
  if (params.type == "ninshin") {
    registerSchedule(
      history,
      ScheduleType.NINSHIN_DETECTION,
      $gameActors.actor(actorId).getActorHistory().lastNinshinTaneoyaType()
    );
  }
  if (history.boteId() > 0) {
  } else {
    if (params.ninshinPlus) {
      registerSchedule(
        history,
        ScheduleType.NINSHIN_DAMAGE,
        params.ninshinPlus
      );
    }
    var currentNinshinRate = actorHistory.countNinshinRate($gameSystem.day());
    if (lastNinshinRate < 100 && currentNinshinRate >= 100 && !params.ninshin) {
      if (!params.ninshin) {
        if (actorId != 7) {
          p("upEroParam　妊娠: 種親" + taneoyaId);
          if (taneoyaId > 0) {
            registerSchedule(
              history,
              ScheduleType.NINSHIN,
              taneoyaId,
              false,
              key
            );
            if (!skipNinshinEvent) {
              $gameSystem
                .timelineManager()
                .push(new TimelineItem(TimelineType.NINSHIN, 0, actorId));
            }
          } else {
            console.error("upEroParam　妊娠 で種親が設定されていません:" + key);
          }
        }
      }
    }
  }
  if (isEroUp) {
    if (params.manko_ero > 0) {
      registerSchedule(
        history,
        ScheduleType.MANKO_ERO,
        Math.trunc(params.nakadashi + plus / 2) + params.manko_ero
      );
    }
    if (params.anal > 0 || params.anal_ero) {
      registerSchedule(
        history,
        ScheduleType.ANAL_ERO,
        Math.trunc(params.anal * 2 + plus / 2) + params.anal_ero
      );
    }
  }
  if (params.skillPoint > 0) {
    $gameVariables.setValue(100 + actorId, params.skillPoint);
    registerSchedule(history, ScheduleType.SKILL_POINT, params.skillPoint);
  }
  // 淫乱度
  if (params.nasty > 0) {
    registerSchedule(history, ScheduleType.NASTY, params.nasty);
  }
  // 服従度
  if (params.obedience > 0) {
    registerSchedule(history, ScheduleType.OBEDIENCE, params.obedience);
  }
  // フェラ
  if (params.fela > 0) {
    registerSchedule(history, ScheduleType.FELA, params.fela);
  }
  // 絶頂回数
  if (params.manko_acme > 0) {
    if (schedule) {
      var acme = schedule.countAcme();
      schedule.setAcme(acme + params.manko_acme);
    } else {
      registerSchedule(history, ScheduleType.ACME, params.manko_acme);
    }
  }
  if (params.anal_acme > 0) {
    registerSchedule(history, ScheduleType.ANAL_ACME, params.anal_acme);
  }
  var count = history.getLastEvent().countAcme();
  p("絶頂回数:" + count);
  $gameVariables.setValue(ACME_VAR_ID, count);
}
function registerSchedule(history, scheduleId, value, allowOverlap, eroUpKey) {
  if (allowOverlap === void 0) {
    allowOverlap = true;
  }
  if (eroUpKey === void 0) {
    eroUpKey = null;
  }
  scheduleId = normalizeScheduleId(scheduleId);
  //p('registerSchedule:' + scheduleId + ' value:' + value)
  var event = history.getLastEvent();
  var schedule = event.findSchedule(scheduleId);
  if (schedule && isOverlap(scheduleId) && allowOverlap) {
    schedule.addValue(value);
    if (eroUpKey) {
      schedule.setEroUpKey(eroUpKey);
    }
    return schedule;
  } else {
    return history.addSchedule(scheduleId, value, eroUpKey);
  }
}
function normalizeScheduleId(scheduleId) {
  switch (scheduleId) {
    case ScheduleType.TANETSUKE:
    case ScheduleType.TANETSUKE_BACK:
    case ScheduleType.TANETSUKE_IN_PERSON:
      return ScheduleType.TANETSUKE;
  }
  switch (scheduleId) {
    case ScheduleType.ANAL_IN_PERSON:
      return ScheduleType.ANAL;
  }
  return scheduleId;
}
function isOverlap(scheduleId) {
  switch (scheduleId) {
    case ScheduleType.ACCE_ON:
      return false;
  }
  return true;
}
function addTimeline(actorId) {
  var item = $gameSystem.timelineManager().currentTimeline();
  if (!item) {
    return;
  }
  item.addResult($gameActors.actor(actorId).getLastHistory());
}
